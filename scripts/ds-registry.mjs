#!/usr/bin/env node
/**
 * ds-registry.mjs — validate the Component Registry against reality, then
 * generate the human catalog `docs/design-system/ComponentRegistry.md`.
 *
 * VALIDATION (the gate — exit 1 on any mismatch):
 *   - every `components/<slug>/` folder is REGISTERED in the registry,
 *   - every registered (non-planned) slug HAS a folder,
 *   - every registered (non-planned) slug is EXPORTED from the design-system barrel.
 * This enforces AI_RULES ("always check the registry; never duplicate a
 * component"): a component added without registering it fails this check.
 *
 * GENERATION: writes the grouped Markdown catalog from the same single source.
 *
 * Usage: `pnpm ds:registry`  (Part 15: Component Validation + Generate DS Index)
 */
import { readdirSync, readFileSync, writeFileSync } from 'node:fs';

const REGISTRY = 'src/shared/design-system/registry/component-registry.ts';
const BARREL = 'src/shared/design-system/index.ts';
const COMPONENTS_DIR = 'src/shared/design-system/components';
const OUT = 'docs/design-system/ComponentRegistry.md';

const registrySrc = readFileSync(REGISTRY, 'utf8');
const barrelSrc = readFileSync(BARREL, 'utf8');

// --- Parse registry entries (uniform single-line objects). ---
const entryRe =
  /\{\s*name:\s*'([^']+)',\s*category:\s*'([^']+)',\s*status:\s*'([^']+)',\s*since:\s*(?:'([^']*)'|(\w+)),\s*slug:\s*(?:'([^']+)'|null),[^]*?description:\s*'([^']*)'/g;
/** Resolve a bare `since` identifier (e.g. `V1`) to its `const X = '...'` value. */
const resolveConst = (id) =>
  (registrySrc.match(new RegExp(`const ${id}\\s*=\\s*'([^']*)'`)) ?? [])[1] ?? id;
const entries = [];
for (const m of registrySrc.matchAll(entryRe)) {
  entries.push({
    name: m[1],
    category: m[2],
    status: m[3],
    since: m[4] ?? resolveConst(m[5]),
    slug: m[6] ?? null,
    description: m[7],
  });
}
if (entries.length === 0) {
  console.error('✗ Could not parse any registry entries — check the entry format.');
  process.exit(1);
}

// --- Reality. ---
const folderSlugs = readdirSync(COMPONENTS_DIR, { withFileTypes: true })
  .filter((d) => d.isDirectory())
  .map((d) => d.name);
const exportedSlugs = [...barrelSrc.matchAll(/export \* from '\.\/components\/([^']+)'/g)].map(
  (m) => m[1],
);
const registeredSlugs = entries.map((e) => e.slug).filter(Boolean);

// --- Validate. ---
const errors = [];
for (const slug of folderSlugs) {
  if (!registeredSlugs.includes(slug)) errors.push(`Folder components/${slug}/ is NOT registered.`);
  if (!exportedSlugs.includes(slug))
    errors.push(`Folder components/${slug}/ is NOT exported from the barrel.`);
}
for (const e of entries) {
  if (e.slug && !folderSlugs.includes(e.slug))
    errors.push(`Registry entry ${e.name} → missing folder components/${e.slug}/.`);
}

console.log(
  `Component Registry — ${entries.length} entries (${registeredSlugs.length} shipped), ` +
    `${folderSlugs.length} folders, ${exportedSlugs.length} barrel exports.`,
);

if (errors.length) {
  console.error(`\n✗ Registry validation failed (${errors.length}):`);
  for (const e of errors) console.error(`   - ${e}`);
  process.exit(1);
}
console.log('✓ Registry ↔ folders ↔ barrel are consistent.');

// --- Generate the catalog. ---
const CATEGORY_ORDER = [
  'primitive',
  'form',
  'layout',
  'navigation',
  'data-display',
  'feedback',
  'overlay',
  'healthcare',
  'analytics',
  'utility',
];
const byCat = (cat) => entries.filter((e) => e.category === cat);
const statusIcon = (s) =>
  ({ stable: '✅', beta: '🧪', experimental: '⚗️', deprecated: '⚠️', planned: '🗓️' })[s] ?? '•';

let md = `<!-- GENERATED by \`pnpm ds:registry\` from src/shared/design-system/registry/component-registry.ts — DO NOT EDIT BY HAND. -->\n\n`;
md += `# ClinicOS — Component Registry\n\n`;
md += `The permanent catalog of every design-system component, generated from the\n`;
md += `type-safe registry. Consume components from \`@shared/design-system\`; never\n`;
md += `duplicate — check this registry first (see [ContributionGuide.md](./ContributionGuide.md)).\n\n`;
md += `**Totals:** ${registeredSlugs.length} shipped · ${entries.length - registeredSlugs.length} planned · ${entries.length} tracked.\n\n`;
for (const cat of CATEGORY_ORDER) {
  const items = byCat(cat);
  if (!items.length) continue;
  md += `## ${cat}\n\n`;
  md += `| Status | Component | Since | Stories | Tests | Description |\n`;
  md += `| --- | --- | --- | --- | --- | --- |\n`;
  for (const e of items) {
    const stories = e.status === 'planned' ? '—' : '✓';
    md += `| ${statusIcon(e.status)} ${e.status} | \`${e.name}\` | ${e.since} | ${stories} | ${e.slug ? '·' : '—'} | ${e.description} |\n`;
  }
  md += `\n`;
}
md += `_Legend: ✅ stable · 🧪 beta · 🗓️ planned. Full props/a11y per component: [Frontend-Bible §8](../Frontend-Bible.md) + Storybook (\`pnpm storybook\`)._\n`;

writeFileSync(OUT, md);
console.log(`✓ Wrote ${OUT}`);
process.exit(0);
